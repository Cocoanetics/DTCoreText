// MARK: - Notification

public extension Notification.Name {
	/// Posted when the content view finishes a layout pass.
	/// The `userInfo` dictionary contains an `"OptimalFrame"` key with an `NSValue` wrapping a `CGRect`.
	static let DTAttributedTextContentViewDidFinishLayout = Notification.Name(DTAttributedTextContentViewDidFinishLayoutNotification)
}

// MARK: - Relayout Mask

/// Option set controlling when the content view relayouts its text on bounds changes.
public struct DTAttributedTextContentViewRelayoutMask: OptionSet, @unchecked Sendable {
	public let rawValue: UInt

	public init(rawValue: UInt) {
		self.rawValue = rawValue
	}

	/// Never relayout automatically.
	public static let never = DTAttributedTextContentViewRelayoutMask([])

	/// Relayout when the width changes.
	public static let onWidthChanged = DTAttributedTextContentViewRelayoutMask(rawValue: 1 << 0)

	/// Relayout when the height changes.
	public static let onHeightChanged = DTAttributedTextContentViewRelayoutMask(rawValue: 1 << 1)
}

// MARK: - Delegate Protocol

/// Protocol to provide custom views for elements in an `AttributedTextContentView`.
/// Also receives notifications before/after drawing.
@objc public protocol DTAttributedTextContentViewDelegate: NSObjectProtocol {

	/// Called before a layout frame is drawn.
	@objc optional func attributedTextContentView(_ attributedTextContentView: DTAttributedTextContentView,
												  willDrawLayoutFrame layoutFrame: CoreTextLayoutFrame,
												  in context: CGContext)

	/// Called after a layout frame is drawn.
	@objc optional func attributedTextContentView(_ attributedTextContentView: DTAttributedTextContentView,
												  didDrawLayoutFrame layoutFrame: CoreTextLayoutFrame,
												  in context: CGContext)

	/// Called before a text block background is drawn. Return `true` to draw the default background fill.
	@objc optional func attributedTextContentView(_ attributedTextContentView: DTAttributedTextContentView,
												  shouldDrawBackgroundFor textBlock: TextBlock,
												  frame: CGRect,
												  context: CGContext,
												  forLayoutFrame layoutFrame: CoreTextLayoutFrame) -> Bool

	/// Provide a custom view for an attachment (e.g. an image view).
	@objc optional func attributedTextContentView(_ attributedTextContentView: DTAttributedTextContentView,
												  viewForAttachment attachment: TextAttachment,
												  frame: CGRect) -> UIView?

	/// Provide a custom view/button for a hyperlink.
	@objc optional func attributedTextContentView(_ attributedTextContentView: DTAttributedTextContentView,
												  viewForLink url: URL,
												  identifier: String,
												  frame: CGRect) -> UIView?

	/// Provide a generic custom view for an attributed string element.
	@objc optional func attributedTextContentView(_ attributedTextContentView: DTAttributedTextContentView,
												  viewForAttributedString string: NSAttributedString,
												  frame: CGRect) -> UIView?
}

// MARK: - Delegate Flags

/// Cached booleans for which delegate methods are implemented.
private struct DelegateFlags {
	var supportsCustomViewsForAttachments = false
	var supportsCustomViewsForLinks = false
	var supportsGenericCustomViews = false
	var supportsNotificationBeforeDrawing = false
	var supportsNotificationAfterDrawing = false
	var supportsNotificationBeforeTextBoxDrawing = false
}

