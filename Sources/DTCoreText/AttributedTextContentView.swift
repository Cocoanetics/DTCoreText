    // MARK: - Drawing (Content Image)

    /// Creates an image from a part of the receiver's content.
    @objc(contentImageWithBounds:options:)
    open func contentImage(with contentBounds: CGRect, options: CoreTextLayoutFrameDrawingOptions)
      -> UIImage?
    {
      UIGraphicsBeginImageContextWithOptions(contentBounds.size, false, 0)

      guard let context = UIGraphicsGetCurrentContext() else {
        return nil
      }

      context.translateBy(x: -contentBounds.origin.x, y: -contentBounds.origin.y)
      self.layoutFrame?.draw(in: context, options: options.rawValue)

      let image = UIGraphicsGetImageFromCurrentImageContext()
      UIGraphicsEndImageContext()

      return image
    }

    // MARK: - Accessibility

    private func invalidateAccessibilityElements() {
      _accessibilityElements = nil
    }

    open override var isAccessibilityElement: Bool {
      get { false }
      set { /* no-op */  }
    }

    open override func accessibilityElementCount() -> Int {
      return accessibilityElements?.count ?? 0
    }

    open override func accessibilityElement(at index: Int) -> Any? {
      guard let elements = accessibilityElements,
        index >= 0, index < elements.count
      else {
        return nil
      }
      return elements[index]
    }

    open override func index(ofAccessibilityElement element: Any) -> Int {
      guard let elements = accessibilityElements else { return NSNotFound }
      let index = elements.firstIndex { obj in
        if let objNS = obj as? NSObject, let elemNS = element as? NSObject {
          return objNS.isEqual(elemNS)
        }
        return false
      }
      return index ?? NSNotFound
    }

    open override var accessibilityElements: [Any]? {
      get {
        if _accessibilityElements == nil, let layoutFrame = self.layoutFrame {
          let generator = LayoutFrameAccessibilityElementGenerator()
          _accessibilityElements = generator.accessibilityElements(for: layoutFrame, view: self) {
            [weak self] (attachment: TextAttachment) -> Any? in
            guard let self else { return NSNull() }
            return AccessibilityViewProxy(textAttachment: attachment, delegate: self) as Any
          }
        }
        return _accessibilityElements
      }
      set {
        _accessibilityElements = newValue
      }
    }

