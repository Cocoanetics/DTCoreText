    // MARK: - Layouter (synchronized)

    /// The layouter used for the receiver. Created automatically when needed.
    @objc open var layouter: CoreTextLayouter? {
      get {
        _lock.lock()
        defer { _lock.unlock() }

        if _layouter == nil, let string = _attributedString {
          _layouter = CoreTextLayouter(attributedString: string)
          _layouter?.shouldCacheLayoutFrames = true
        }
        return _layouter
      }
      set {
        _lock.lock()
        defer { _lock.unlock() }
        _layouter = newValue
      }
    }

    /// The layout frame used for the receiver. Created automatically when needed.
    @objc open var layoutFrame: CoreTextLayoutFrame? {
      get {
        _lock.lock()
        defer { _lock.unlock() }

        guard let theLayouter = layouter else { return _layoutFrame }

        if _layoutFrame == nil {
          var rect = bounds.inset(by: _edgeInsets)

          if rect.size.width <= 0 {
            return nil
          }

          if _layoutFrameHeightIsConstrainedByBounds {
            if rect.size.height <= 0 {
              return nil
            }
          } else {
            rect.size.height = CGFloat(CGFLOAT_HEIGHT_UNKNOWN)
          }

          _layoutFrame = theLayouter.layoutFrame(with: rect, range: NSRange(location: 0, length: 0))
          _layoutFrame?.numberOfLines = _numberOfLines
          _layoutFrame?.lineBreakMode = _lineBreakMode
          _layoutFrame?.truncationString = _truncationString

          // Post notification for initial layout
          let neededSize = CGSize(
            width: _layoutFrame!.frame.size.width + _edgeInsets.left + _edgeInsets.right,
            height: _layoutFrame!.frame.maxY + _edgeInsets.bottom
          )

          let optimalFrame = CGRect(
            x: frame.origin.x, y: frame.origin.y,
            width: neededSize.width, height: neededSize.height)
          let userInfo: [String: Any] = ["OptimalFrame": NSValue(cgRect: optimalFrame)]

          NotificationCenter.default.post(
            name: .DTAttributedTextContentViewDidFinishLayout,
            object: self,
            userInfo: userInfo
          )

          // Set text block handler if delegate supports it
          if _delegateFlags.supportsNotificationBeforeTextBoxDrawing {
            _layoutFrame?.textBlockHandler = {
              [weak self] textBlock, blockFrame, context, shouldDrawDefaultBackground in
              guard let self else { return }
              let result =
                self._delegate?.attributedTextContentView?(
                  self,
                  shouldDrawBackgroundFor: textBlock,
                  frame: blockFrame,
                  context: context,
                  forLayoutFrame: self._layoutFrame!
                ) ?? true

              shouldDrawDefaultBackground.pointee = ObjCBool(result)
            }
          }

          invalidateAccessibilityElements()
        }

        return _layoutFrame
      }
      set {
        _lock.lock()
        defer { _lock.unlock() }

        if _layoutFrame !== newValue {
          removeAllCustomViewsForLinks()

          if newValue != nil {
            setNeedsLayout()
            setNeedsDisplay(bounds)
          }
          _layoutFrame = newValue
          invalidateAccessibilityElements()
        }
      }
    }

    /// Delegate that provides custom behavior for the receiver.
    @objc open weak var delegate: (any DTAttributedTextContentViewDelegate)? {
      get { _delegate }
      set {
        _delegate = newValue

        _delegateFlags.supportsCustomViewsForAttachments =
          newValue?.responds(
            to: #selector(
              DTAttributedTextContentViewDelegate.attributedTextContentView(
                _:viewForAttachment:frame:))) ?? false
        _delegateFlags.supportsCustomViewsForLinks =
          newValue?.responds(
            to: #selector(
              DTAttributedTextContentViewDelegate.attributedTextContentView(
                _:viewForLink:identifier:frame:))) ?? false
        _delegateFlags.supportsGenericCustomViews =
          newValue?.responds(
            to: #selector(
              DTAttributedTextContentViewDelegate.attributedTextContentView(
                _:viewForAttributedString:frame:))) ?? false
        _delegateFlags.supportsNotificationBeforeDrawing =
          newValue?.responds(
            to: #selector(
              DTAttributedTextContentViewDelegate.attributedTextContentView(
                _:willDrawLayoutFrame:in:))) ?? false
        _delegateFlags.supportsNotificationAfterDrawing =
          newValue?.responds(
            to: #selector(
              DTAttributedTextContentViewDelegate.attributedTextContentView(
                _:didDrawLayoutFrame:in:))) ?? false
        _delegateFlags.supportsNotificationBeforeTextBoxDrawing =
          newValue?.responds(
            to: #selector(
              DTAttributedTextContentViewDelegate.attributedTextContentView(
                _:shouldDrawBackgroundFor:frame:context:forLayoutFrame:))) ?? false

        if !_delegateFlags.supportsCustomViewsForLinks && !_delegateFlags.supportsGenericCustomViews
        {
          removeAllCustomViewsForLinks()
        }

        // Don't draw images if delegate provides custom views
        if _delegateFlags.supportsGenericCustomViews
          || _delegateFlags.supportsCustomViewsForAttachments
        {
          _shouldDrawImages = false
        } else {
          _shouldDrawImages = true
        }

        updateRenderSnapshot()
      }
    }

